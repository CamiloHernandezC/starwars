package com.meli.starwars.utils;

public class Position {

	private double x, y;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public Position(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}

}
